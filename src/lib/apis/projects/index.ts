import { WEBUI_API_BASE_URL } from '$lib/constants';

export const createNewProject = async (token: string, title: string, description: string) => {
	let error = null;

	const res = await fetch(`${WEBUI_API_BASE_URL}/projects/`, {
		method: 'POST',
		headers: {
			Accept: 'application/json',
			'Content-Type': 'application/json',
			authorization: `Bearer ${token}`
		},
		body: JSON.stringify({
			title: title,
			description: description
		})
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			error = err;
			console.error(err);
			return null;
		});

	if (error) {
		throw error;
	}

	return res;
};

